namespace Magetrend\AbandonedCart\Model\Config\Source;

/**
 * Rule type source
 *
 * @category MageTrend
 * @package  Magetend/AbandonedCart
 * @author   Edvinas Stulpinas <edwin@magetrend.com>
 * @license  http://opensource.org/licenses/osl-3.0.php Open Software License (OSL 3.0)
 * @link     https://www.magetrend.com/magento-2-abandoned-cart
 */
class RuleType implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        $options = $this->toArray();
        $optionArray = [];
        foreach ($options as $value => $label) {
            $optionArray[] = [
                'value' => $value,
                'label' => $label
            ];
        }

        return $optionArray;
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [
            \Magetrend\AbandonedCart\Model\Rule::TYPE_ABANDONED_CART => __('Abandoned Cart Email'),
            \Magetrend\AbandonedCart\Model\Rule::TYPE_FOLLOW_UP => __('Follow Up for not Paid Orders'),
            \Magetrend\AbandonedCart\Model\Rule::TYPE_BAR => __('Top Bar'),
        ];
    }
}
